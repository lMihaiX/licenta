  void signUp(String email, String password, String rool, String numeCopil, String numeParinte) async {
    CircularProgressIndicator();
    if (_formkey.currentState!.validate()) {
      await _auth
          .createUserWithEmailAndPassword(email: email, password: password)
          .then((value) => {postDetailsToFirestore(email, rool, numeCopil, numeParinte)})
          .catchError((e) {});
    }
  }

  postDetailsToFirestore(String email, String rool, String numeCopil, String numeParinte) async {
    FirebaseFirestore firebaseFirestore = FirebaseFirestore.instance;
    User? user = _auth.currentUser;
    UserModel userModel = UserModel();
    userModel.email = email;
    userModel.uid = user!.uid;
    userModel.wrool = rool;
    userModel.numeCopil = numeCopil;
    userModel.numeParinte = numeParinte;
    await firebaseFirestore
        .collection("users")
        .doc(user.uid)
        .set(userModel.toMap());

    Navigator.pushReplacement(
        context, MaterialPageRoute(builder: (context) => LoginPage()));
  }
